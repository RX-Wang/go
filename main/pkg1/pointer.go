/**
	指针相关
*/
package pkg1

import "fmt"

func init() {
	s := "i'm a string"
	fmt.Printf("var == *(&var): %t\n", s == *(&s))
	fmt.Printf("s's address：%p\n", &s)
	var p *string = &s
	var q *string
	fmt.Printf("the pointer p is：%p\n", p)
	fmt.Printf("the pointer p's value is：%s\n", *p)
	*p = "this is a new string"
	fmt.Printf("the new s is:%s\n", s)
	fmt.Printf("the new pointer p's value is：%s\n", s)
	fmt.Printf("q:%p\n", q)
	fmt.Printf("q's value is :%s\n", *q)
}